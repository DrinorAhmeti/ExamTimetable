package tema;

public class Provimi {
	int id;
	String name;
	String Profesori;
	int nrFleteparaqitjeve;
	boolean oral;
	char drejtimi;
	
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public char getDrejtimi() {
		return drejtimi;
	}
	public void setDrejtimi(char drejtimi) {
		this.drejtimi = drejtimi;
	}
	public boolean isOral() {
		return oral;
	}
	public void setOral(boolean oral) {
		this.oral = oral;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getProfesori() {
		return Profesori;
	}
	public void setProfesori(String profesori) {
		Profesori = profesori;
	}
	public int getNrFleteparaqitjeve() {
		return nrFleteparaqitjeve;
	}
	public void setNrFleteparaqitjeve(int nrFleteparaqitjeve) {
		this.nrFleteparaqitjeve = nrFleteparaqitjeve;
	}
	
	
	

}
